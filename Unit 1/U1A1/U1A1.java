//
// This program will print a face
// to the console window.

public class U1A1 {

	public static void main (String[] args) {

		System.out.println("  /////"); // Hair
		System.out.println(" |     |"); // Forehead
		System.out.println(" | \" \" |"); // Eyebrows
		System.out.print(" | o o | \n"); // Eyes
		System.out.println("(|  ^  |)"); // Ears and nose
		System.out.println(" | \\_/ |"); // Mouth
		System.out.println("  -----"); // Chin

	}

}


